    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int coeA = Integer.parseInt(jTextField1.getText());
    int coeB = Integer.parseInt(jTextField2.getText());
    int coeC = Integer.parseInt(jTextField3.getText());
    int Delta = (coeB*coeB) - (4*coeA*coeC);
    double x1=0,x2=0;
    
    if (Delta>0) {
        x1 =  (-coeB + Math.sqrt(Delta) )/2*coeA;
        x2 =  (-coeB - Math.sqrt(Delta) )/2*coeA;

        jLabel7.setText(Double.toString(x1));
        jLabel8.setText(Double.toString(x2));
        jLabel9.setText("Raizes Distintas");
    }else if (Delta<0) {
    
        jLabel7.setText("0");
        jLabel8.setText("0");
        jLabel9.setText("Raizes Imaginarias");
    }else if (Delta==0) {
        
        x1 =  (-coeB + Math.sqrt(Delta) )/2*coeA;
        
        jLabel7.setText(Double.toString(x1));
        jLabel8.setText("0");
        jLabel9.setText("Raizes Imaginarias");
    }
    
    
    }//GEN-LAST:event_jButton1ActionPerformed


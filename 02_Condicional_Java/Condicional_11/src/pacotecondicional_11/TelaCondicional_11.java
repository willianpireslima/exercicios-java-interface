    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int ladA = Integer.parseInt(jTextField1.getText());
    int ladB = Integer.parseInt(jTextField2.getText());
    int ladC = Integer.parseInt(jTextField3.getText());
    
    if (ladA < ladB + ladC && ladB < ladA + ladC && ladC < ladA + ladB) {
        jLabel6.setText("Sim");
        
        if (ladA == ladB && ladB == ladC) {
            jLabel7.setText("Equilatero");
        }else if (ladA == ladB || ladB == ladC || ladA == ladC ) {
            jLabel7.setText("Isosceles");
        }else if (ladA != ladB || ladB != ladC || ladA != ladC ) {
            jLabel7.setText("Escaleno");
        }
    }else {
    jLabel6.setText("Nao");
    }
             
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ladA = Integer.parseInt(jTextField1.getText());
        int ladB = Integer.parseInt(jTextField2.getText());
        int ladC = Integer.parseInt(jTextField3.getText());

        if (ladA < ladB + ladC && ladB < ladA + ladC && ladC < ladA + ladB) {
            jLabel6.setText("Sim");

            if (ladA*ladA == ladB*ladB + ladC*ladC) {
                jLabel7.setText("Triangulo Retangulo");
            }else if (ladA*ladA > ladB*ladB + ladC*ladC) {
                jLabel7.setText("Obtusangulo");
            }else if (ladA*ladA < ladB*ladB + ladC*ladC ) {
                jLabel7.setText("Acutangulo");
            }
        }else {
            jLabel6.setText("Nao");
        }

    }//GEN-LAST:event_jButton1ActionPerformed


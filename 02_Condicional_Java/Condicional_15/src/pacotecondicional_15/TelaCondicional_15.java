    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    final int SALARIOMINIMO = 1200;
    int depe = Integer.parseInt(jTextField1.getText());
    int sala = Integer.parseInt(jTextField2.getText());
    int impo = Integer.parseInt(jTextField3.getText());
    float impoB =0;
    String mensImpo = "";
    
    if (sala> 12*SALARIOMINIMO) {
        impoB = 0.30f*sala;
    }else if (sala> 5*SALARIOMINIMO && sala < 5*SALARIOMINIMO) {
        impoB = 0.08f*sala;
    }else if (sala < 5*SALARIOMINIMO) {
        impoB = 0;
    }
    
    impoB = impoB - depe*300;
    jLabel5.setText(Float.toString(impoB));
    mensImpo = (impo - impoB > 0)? "Imposto a Receber" : "Imposto a Pagar";
    jLabel7.setText(mensImpo);

    }//GEN-LAST:event_jButton1ActionPerformed


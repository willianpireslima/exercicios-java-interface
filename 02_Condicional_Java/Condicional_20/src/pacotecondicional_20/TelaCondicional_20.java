    String loc = jComboBox2.getSelectedItem().toString();
    int valor = Integer.parseInt(jTextField1.getText());
    float valorF = 0;
    
    if (dia == "Segunda" || dia == "Terça" || dia == "Quinta" ) {
    valorF = 0.60f*valor;
    }
    
    if (loc == "Lancamento") {
     valorF = 1.15f*valor;
    }
    
    jLabel6.setText(Float.toString(valorF));
    jTextField1.setText("");

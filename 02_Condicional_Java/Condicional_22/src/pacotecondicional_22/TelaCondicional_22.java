    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    final float PI = 3.14f;    
    float raio = Float.parseFloat(jTextField1.getText());
    float altu = Float.parseFloat(jTextField2.getText());
    String tipo = jComboBox1.getSelectedItem().toString();
    double volu = 0,area = 0;

    if (tipo == "Cone Reto") {
        volu = (PI*raio*raio*altu)/3;
        area = PI*raio*Math.sqrt(raio*raio+altu*altu);
    }else if (tipo == "Cilindro") {
        volu = (PI*raio*raio*altu);
        area = 2*raio*altu;
    }else if (tipo == "Esfera") {
        volu = (4/3)*PI*raio*raio*raio;
        area = 4*PI*raio*raio;
    }

    jLabel5.setText(Double.toString(volu));
    jLabel7.setText(Double.toString(area));
    jTextField1.setText("");
    jTextField2.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed



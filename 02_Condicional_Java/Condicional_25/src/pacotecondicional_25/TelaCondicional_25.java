    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int num1 = Integer.parseInt(jTextField1.getText());
    int num2 = Integer.parseInt(jTextField2.getText());
    int num3 = Integer.parseInt(jTextField3.getText());
    String media = jComboBox1.getSelectedItem().toString();
    double resul = 0;
    
    if (media == "Aritmetica") {
        resul = (num1 + num2 + num3)/3;
    }else if (media == "Ponderada") {
        resul = (3*num1 + 3*num2 + 4*num3)/3;
    }else if (media == "Harmonica") {
        resul = Math.cbrt(num1*num2*num3);
    }else if (media == "Geometrica") {
        resul = 3/( 1/num1 + 1/num2 + 1/num3);
    }else if (media == "Quadratica") {
        resul = Math.sqrt(num1*num1+ num2*num2 + num3*num3)/3;
    }
    
    jLabel7.setText(Double.toString(resul));
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed


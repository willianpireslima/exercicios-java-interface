    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
    int not1 = Integer.parseInt(jTextField1.getText());
    int not2 = Integer.parseInt(jTextField2.getText());
    int not3 = Integer.parseInt(jTextField3.getText());
    int media = Integer.parseInt(jTextField4.getText());    
         
    float resul = (not1 + 2*not2 + 3*not3 + media)/7;

    if (resul>9 && resul <=10) {
            jLabel7.setText("A");
            jLabel8.setText("Aprovado");
    }else if (resul>7.5 && resul <=9) {
            jLabel7.setText("B");
            jLabel8.setText("Aprovado");
    }else if (resul>6 && resul <=7.5) {
            jLabel7.setText("C");
            jLabel8.setText("Aprovado");
    }else if (resul>4 && resul <=6) {
            jLabel7.setText("D");
            jLabel8.setText("Reprovado");
    }else if (resul <=4) {
            jLabel7.setText("E");
            jLabel8.setText("Reprovado");
    }

    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed


    final int salMin = 545,valorHoraExtra = 10;
    
    int salaHoraExtra = hora*valorHoraExtra;
    int salaBruto = 3*salMin+ salaHoraExtra;
    
    float descontINSS = (salaBruto>1500)? 0.12f*salaBruto: 0;
    float descontRenda= (salaBruto>2000)? 0.20f*salaBruto: 0;
    
    float salaLiq = salaBruto -descontINSS -descontRenda;
    
    jLabel4.setText(Float.toString(salaLiq));
    jTextField1.setText("");

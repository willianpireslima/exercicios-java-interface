    if (saldo <1000) {
        jLabel4.setText("Basica");
    }else if (saldo>1000 && saldo <2000) {
        jLabel4.setText("Prata");
    }else if (saldo>2001 && saldo <=3500) {
        jLabel4.setText("Ouro");
    }else if (saldo>3500) {
        jLabel4.setText("Premio");
    }
    jTextField1.setText("");

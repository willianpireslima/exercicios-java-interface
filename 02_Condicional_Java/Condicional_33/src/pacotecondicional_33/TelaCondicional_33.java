    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int litro = Integer.parseInt(jTextField1.getText());
    String combu = jComboBox1.getSelectedItem().toString();
    float preco =0;
    
    if (combu == "Alcool") {
        preco = 1.799f*litro;
    } else if (combu == "Diesel") {
        preco = 1.899f*litro;
    }else if (combu == "Gasolina") {
        preco = 2.799f*litro;
    }
    jLabel5.setText(Float.toString(preco));
    jTextField1.setText("");
    
    }//GEN-LAST:event_jButton1ActionPerformed

